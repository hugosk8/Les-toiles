                    <p class="section-descr mb-0 mb-sm-30 fs-18">
                        La démarche zéro déchet vise à réduire au maximum la production de déchets en favorisant le réemploi, la réparation et le recyclage des objets du quotidien. En couture, cela se traduit par la création de vêtements et d’accessoires à partir de tissus de récupération, la réparation des vêtements abîmés et la fabrication d’articles durables comme des sacs à vrac, des lingettes réutilisables ou des essuie-tout en tissu. Cette approche permet non seulement de faire des économies, mais aussi de consommer de manière plus responsable.
                        <br><br>
                        Quatre fois par an, j’anime des ateliers couture zéro déchet dans les villes autour de Lille. Ces ateliers, organisés à la demande des mairies et des centres sociaux de Roubaix, Wattrelos et d’autres localités du Nord, accueillent jusqu’à dix participants et durent deux heures. Pendant ces sessions, nous explorons ensemble les différentes techniques de couture et les astuces pour donner une seconde vie à nos vêtements et accessoires. Chaque atelier est une opportunité de partager des connaissances et de développer des compétences pratiques tout en s’amusant.
